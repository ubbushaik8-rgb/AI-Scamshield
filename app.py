def _reset_stats_if_new_day():
    today = datetime.utcnow().date()
    if STATS["date"] != today:
        STATS.update({
            "date": today, "total_scans": 0, "scams_detected": 0,
            "phishing_blocked": 0, "upi_flagged": 0, "urls_flagged": 0,
            "by_type": {}, "by_risk": {"HIGH": 0, "MEDIUM": 0, "LOW": 0, "SAFE": 0},
        })


def _record_scan(scam_type: str, risk_level: str):
    _reset_stats_if_new_day()
    STATS["total_scans"] += 1
    if risk_level in ("HIGH", "MEDIUM"):
        STATS["scams_detected"] += 1
    if scam_type == "Phishing Email":
        STATS["phishing_blocked"] += 1
    if scam_type == "UPI Fraud":
        STATS["upi_flagged"] += 1
    if scam_type == "Malicious URL":
        STATS["urls_flagged"] += 1
    STATS["by_type"][scam_type] = STATS["by_type"].get(scam_type, 0) + 1
    STATS["by_risk"][risk_level] = STATS["by_risk"].get(risk_level, 0) + 1


