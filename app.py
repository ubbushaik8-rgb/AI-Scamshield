def _local_chat_reply(message: str) -> str:
    low = message.lower()
    for keywords, reply in CANNED_CHAT_RESPONSES:
        if any(k in low for k in keywords):
            return reply
    return DEFAULT_CHAT_REPLY


@app.route("/api/chat", methods=["POST"])
def api_chat():
    body = request.get_json(silent=True) or {}
    message = (body.get("message") or "").strip()
    session_id = body.get("session_id") or str(uuid.uuid4())

    if not message:
        return jsonify({"error": "Message is required."}), 400

    history = CHAT_SESSIONS.setdefault(session_id, [])
    history.append({"role": "user", "content": message})

    reply = None
    if watsonx_client.is_configured():
        try:
            prompt = _build_chat_prompt(history)
            reply = watsonx_client.generate(prompt, max_new_tokens=250, temperature=0.5)
        except Exception as e:
            log.warning("watsonx.ai chat generation failed, using fallback: %s", e)

    if not reply:
        reply = _local_chat_reply(message)

    history.append({"role": "assistant", "content": reply})
    # Keep sessions bounded
    if len(history) > 20:
        CHAT_SESSIONS[session_id] = history[-20:]

    return jsonify({"reply": reply, "session_id": session_id})


def _build_chat_prompt(history):
    convo = "\n".join(f"{'User' if h['role']=='user' else 'Assistant'}: {h['content']}" for h in history[-6:])
    return (
        "You are the AI Cyber Assistant for AI ScamShield, a friendly and knowledgeable "
        "assistant that helps users in India understand and avoid online scams, phishing, "
        "UPI fraud, and cyber threats. Keep replies concise (3-5 sentences), practical, "
        "and reassuring.\n\n"
        f"{convo}\nAssistant:"
    )


