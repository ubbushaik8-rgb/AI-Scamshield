# API: ANALYZE  (Agents 1, 2, 3, 4)
# ══════════════════════════════════════════════════════════════════════════
@app.route("/api/analyze", methods=["POST"])
def api_analyze():
    body = request.get_json(silent=True) or {}
    message = (body.get("message") or "").strip()
    scan_type = body.get("type", "all")

    if not message:
        return jsonify({"error": "Message is required."}), 400
    if len(message) > 5000:
        return jsonify({"error": "Message exceeds 5000 character limit."}), 400

    # Agent 1 — classify
    scam_type = classify_scam_type(message, scan_type)

    # Agent 2 — threat indicators + risk score
    indicator_result = analyze_indicators(message)
    reasons = indicator_result["reasons"]
    score = indicator_result["score"]

    verdict, risk_level = determine_verdict(score)
    if scam_type == "No Specific Scam Pattern" and verdict == "GENUINE":
        scam_type = "None Detected"

    # Agent 3 — RAG knowledge retrieval
    knowledge = retrieve_knowledge(message, scam_type)

    # Agent 4 — safety advice
    safety_tips = safety_tips_for(scam_type)
    report_to = report_to_for(scam_type)

    # Explanation text — try Granite via watsonx, fall back to local template
    explanation = None
    cyber_guidance = None
    if watsonx_client.is_configured():
        try:
            prompt = _build_analysis_prompt(message, scam_type, verdict, risk_level, reasons, knowledge["combined_text"])
            explanation = watsonx_client.generate(prompt, max_new_tokens=300)
        except Exception as e:
            log.warning("watsonx.ai explanation generation failed, using fallback: %s", e)

    if not explanation:
        explanation = build_explanation_fallback(verdict, scam_type, reasons)
        cyber_guidance = None

    _record_scan(scam_type, risk_level)

    return jsonify({
        "verdict": verdict,
        "scam_type": scam_type,
        "risk_level": risk_level,
        "confidence": round(min(97.0, max(52.0, score + random.uniform(-3, 5))), 1),
        "explanation": explanation,
        "knowledge": knowledge["combined_text"] or "No specific knowledge-base matches found.",
        "reasons": reasons,
        "safety_tips": safety_tips,
        "report_to": report_to,
        "cyber_guidance": cyber_guidance,
        "agent_source": "IBM Granite (watsonx.ai)" if watsonx_client.is_configured() else "Local Reasoning Engine",
    })


def _build_analysis_prompt(message, scam_type, verdict, risk_level, reasons, knowledge_text):
    reasons_block = "\n".join(f"- {r}" for r in reasons) or "- None specifically flagged."
    return (
        "You are a cybersecurity analyst assistant for AI ScamShield, an Indian "
        "scam/phishing detection tool. Given the structured analysis below, write a "
        "clear, 3-4 sentence plain-language explanation for a non-technical user of "
        "why the message was classified this way. Be concrete and reference the "
        "specific red flags. Do not repeat these instructions in your answer.\n\n"
        f"Message analysed: \"{message}\"\n"
        f"Classification: {scam_type}\n"
        f"Verdict: {verdict} (Risk: {risk_level})\n"
        f"Detected indicators:\n{reasons_block}\n\n"
        f"Relevant cybersecurity knowledge base context: {knowledge_text}\n\n"
        "Explanation:"
    )


