@app.route("/api/security-report", methods=["POST"])
def api_security_report():
    body = request.get_json(silent=True) or {}
    analysis = body.get("analysis_result") or {}

    if not analysis:
        return jsonify({"error": "analysis_result is required."}), 400

    if watsonx_client.is_configured():
        try:
            prompt = _build_report_prompt(analysis)
            report = watsonx_client.generate(prompt, max_new_tokens=500)
            return jsonify({"report": report, "generated_by": "IBM Granite (watsonx.ai)"})
        except Exception as e:
            log.warning("watsonx.ai report generation failed, using fallback: %s", e)

    report = _build_report_fallback(analysis)
    return jsonify({"report": report, "generated_by": "Local Reasoning Engine"})


def _build_report_prompt(analysis):
    return (
        "You are a cybersecurity analyst. Write a formal, structured Threat "
        "Intelligence Report (with sections: Summary, Classification, Risk "
        "Assessment, Indicators, Recommended Actions) based on this scam analysis "
        "data. Keep it under 300 words, professional tone.\n\n"
        f"Data: {analysis}\n\nReport:"
    )


def _build_report_fallback(analysis):
    verdict = analysis.get("verdict", "UNKNOWN")
    scam_type = analysis.get("scam_type", "Unknown")
    risk = analysis.get("risk_level", "UNKNOWN")
    confidence = analysis.get("confidence", "N/A")
    reasons = analysis.get("reasons", [])
    tips = analysis.get("safety_tips", [])
    report_to = analysis.get("report_to", [])
    timestamp = datetime.utcnow().strftime("%Y-%m-%d %H:%M UTC")

    reasons_txt = "\n".join(f"  {i+1}. {r}" for i, r in enumerate(reasons)) or "  None specifically flagged."
    tips_txt = "\n".join(f"  - {t}" for t in tips)
    report_txt = "\n".join(f"  - {a}" for a in report_to)

    return (
        f"THREAT INTELLIGENCE REPORT\n"
        f"Generated: {timestamp}\n"
        f"{'='*50}\n\n"
        f"1. SUMMARY\n"
        f"   Verdict: {verdict}  |  Risk Level: {risk}  |  Confidence: {confidence}%\n"
        f"   Classification: {scam_type}\n\n"
        f"2. THREAT INDICATORS\n{reasons_txt}\n\n"
        f"3. RECOMMENDED ACTIONS\n{tips_txt}\n\n"
        f"4. REPORT TO\n{report_txt}\n\n"
        f"{'='*50}\n"
        f"Generated by AI ScamShield Local Reasoning Engine.\n"
        f"Configure WATSONX_API_KEY / WATSONX_PROJECT_ID for IBM Granite-generated reports."
    )


